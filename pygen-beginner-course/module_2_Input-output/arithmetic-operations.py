"""
Arithmetic Operations
Write a program that calculates the sum, difference, and product of two integers entered from the keyboard.

Input format
The program receives two integers as input, each on a separate line.

Output format
The program should output the sum, difference, and product of the entered numbers, each on a separate line.
"""

a = int(input())
b = int(input())
print(a, "+", b, "=", a + b)
print(a, "-", b, "=", a - b)
print(a, "*", b, "=", a * b)
